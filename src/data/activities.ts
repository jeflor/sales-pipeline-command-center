// Per-lead full activity history (we layer some narrative-style entries for the drawer)
export function activityForLead(leadId: string): Activity[] {
  const main = activities.filter((a) => a.leadId === leadId);
  // Add background activity to make timelines feel deep
  const filler: Activity[] = [];
  if (main.length === 0) return [];
  const owner = main[0].ownerId;
  filler.push(
    {
      id: `${leadId}-f1`,
      leadId,
      type: "email_sent",
      ownerId: owner,
      at: daysAgo(8),
      summary: "Sent: Discovery follow-up + agenda",
    },
    {
      id: `${leadId}-f2`,
      leadId,
      type: "call",
      ownerId: owner,
      at: daysAgo(11),
      summary: "Call (28 min) — Discovery",
      detail: "Confirmed buying process, surfaced 2 stakeholders.",
    },
    {
      id: `${leadId}-f3`,
      leadId,
      type: "stage_change",
      ownerId: owner,
      at: daysAgo(11),
      summary: "Moved from New Lead → Contacted",
    },
    {
      id: `${leadId}-f4`,
      leadId,
      type: "email_received",
      ownerId: owner,
      at: daysAgo(15),
      summary: "Inbound reply: Asked to schedule discovery",
    },
    {
      id: `${leadId}-f5`,
      leadId,
      type: "email_sent",
      ownerId: owner,
      at: daysAgo(16),
      summary: "Sent: Initial outreach",
    },
  );
  return [...main, ...filler].sort((a, b) => (a.at < b.at ? 1 : -1));
}
